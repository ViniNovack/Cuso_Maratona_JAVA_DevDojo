public class Exercicio_1 {
    public static void main(String[] args){
        int numero_tabula = 5;
        int limite = 11;
        for(int i = 0; i < limite; i++){
            System.out.println(numero_tabula + " X " + i + " = " + (numero_tabula * i));
        }
        System.out.println("\nFIM");
    }
}
